# Első feladat
# Készíts egy osztályt négylábú állatoknak legalább három tetszőleges tulajdonsággal és egy metódussal.

# Második feladat 
# Készíts az első feladatban készített osztálynak egy róka alosztályt. A konstruktor legyen ugyanaz, csak kapjon két extra metódust

# Harmadik feladat
# Készíts a második feladatban készített osztálynak egy újabb alosztályt, legyen a konstruktor is módosítva úgy hogy tartalmazza, az összes szülő osztály konstruktorainak elemeit.
# Tartalmazzon két újabb metódust, az egyik egy getter a másik setter típusú legyen. Az osztály a vörös róka legyen.

# Negyedik feladat
# A vörös róka osztályból hozz létre egy objektumot. Az objektumot és/vagy az osztályt módosítsd úgy, hogy kiderüljön az ezeréves kérdésre a válasz: mit mond a róka?
